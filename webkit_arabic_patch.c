unsigned long
findpatterns (FILE * fil, unsigned char *pattern, unsigned long length,
	      unsigned long *locations, unsigned long number)
{
  unsigned long found = 0;
  unsigned long i = 0;
  unsigned long j = 0;
  unsigned long flag = 0;
  unsigned char *buffer = NULL;
  unsigned long num = 0;
  fseek (fil, 0, SEEK_SET);
  buffer = malloc (filesize * sizeof (unsigned char));
  fread (buffer, 1, filesize, fil);
  //printf ("%ld bytes read in\n", );
  //printf ("size of file %ld \n", filesize);
  //fflush (stdout);
  for (i = 0; i < filesize - length; i++)

    {
      flag = -1;
      for (j = 0; j < length; j++)
	if (buffer[i + j] != pattern[j])

	  {
	    flag = 0;
	    break;
	  }
      if (flag == -1)

	{
	  //printf ("Found pattern!!!\n");
	  locations[num++] = i;
	  found++;
	}
    }
  free (buffer);
  return found;
}
unsigned long
replace (FILE * fil, unsigned char *pattern, unsigned char *replacement,
	 unsigned long length)
{
  unsigned long replaced = 0;
  unsigned long locations[] = { 0 };
  findpatterns (fil, pattern, length, locations, 1);
  //printf ("replacing....!!\n");
  if (locations[0] != 0)
    {
      fseek (fil, locations[0], SEEK_SET);
      fwrite (replacement, 1, length, fil);
      replaced++;
    }
  return replaced;
}

